public class Casting2 {
    public static void main(String[] args) {
        char a='a';
        char b='b';
        int ab= a+b;


        System.out.println("Carattere 1 = " +a);
        System.out.println("Carattere 2 = " +b);
        // somma in valore ascii
        System.out.println("Somma dei caratteri in valore ASCII = " +ab);


    }
}
